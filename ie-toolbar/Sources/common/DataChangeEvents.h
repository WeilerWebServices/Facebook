#ifndef DATACHANGEEVENTS_H
#define DATACHANGEEVENTS_H

namespace facebook{
/**
 *  Enum contains all the identifiers
 *  used internally in IE Toolbar 
 *  application.
 */
enum DataChangeEvents {
  // this group of event is
  // used to inform about any change in
  // notifications count
  FBM_POKES_COUNT_UPDATED = 0,
  FBM_MESSAGES_CONT_UPDATED,
  FBM_REQUESTS_COUNT_PDATED,
  FBM_EVENTS_INVINTAIONS_COUNT_UPDATED,
  FBM_GROUPS_INVINTAIONS_COUNT_UPDATED,
  FBM_MY_FRIENDS_LIST_UPDATED, // fired if any updates in friends list

  // notifications about me
  // fired only if some of the parameters 
  // has a greater value
  FBM_I_HAVE_NEW_FRIEND_REQUEST,
  FBM_I_HAVE_NEW_MESSAGE, 
  FBM_I_HAVE_NEW_POKE,   
  FBM_I_HAVE_EVENT_NEW_INVINTAION,
  FBM_I_HAVE_NEW_GROUP_INVINTATION,
  FBM_I_HAVE_NEW_FRIEND,  
  FBM_I_HAVE_NEW_WALL_POST, 
  FBM_MY_STATUS_UPDATED,

  // notifications about my friends
  FBM_FRIEND_UPDATED_PROFILE,
  FBM_FRIEND_UPDATED_STATUS,
  FBM_FRIEND_ADDED_PHOTO_TO_ALBUM,
  FBM_FRIEND_CREATED_ALBUM,
  FBM_SOMEONE_WROTE_ON_A_FRIENDS_WALL,
  FBM_FRIEND_WROTE_NOTE,

  // other internal notifications
  FBM_JUST_LOGGED_IN,  // fired just after user did login
  FBM_LOGOUT_INITIATED,  // fired just after user did logout 
  FBM_LOADING_DATA, // fired just before data retrival
  FBM_UPDATE_VIEW, // fired just after view was changed
  FBM_DOWNLOAD_NEW_VERSION,
  FBM_INSTALL_NEW_VERSION
};

}// !namespace facebook

#endif