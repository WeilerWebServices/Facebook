#ifndef CLIENTSERVICEUI_H
#define CLIENTSERVICEUI_H

#include "../data/ServiceData.h"
#include "../data/ChangesTypes.h"


namespace facebook{

/**
 * Shows modal login dialog
 *
 * @param loginUrl url to Facebook login page (by value)
 * @param parentWindow- handle of parent window (by value)
 *
 * @return https cookies string with login information
 */
void showLoginDialog(const String& loginUrl, ULONG parentWindow, 
                     String& cookies);


/**
 * Shows notification popup, when user own data changed
 *
 * @param changeID - id of change event (by ref)
 * @param sourceData - changed data (by ref)
 */
void showSelfNotification(const DataChangeEvents& changeID, 
           const ServiceData& sourceData);

/**
 * Shows notification popup, when user's friends data changed
 *
 * @param changeID - id of change event (by ref)
 * @param friendsIds - ids of friends whose data changed(by ref)
 * @param sourceData - changed data (by ref)
 */
void showFriendsChangePopup(const DataChangeEvents& changeID,
                const FriendsIDs& friendsIds, 
          const ServiceData& sourceData);

/**
 * Shows notification popup, when user's friends albums changed
 *
 * @param changeID - id of change event (by ref)
 * @param albumsIds - ids of changed albums(by ref)
 * @param sourceData - changed data (by ref)
 */
void showAlbumsChangePopup(const DataChangeEvents& changeID,
            const AlbumsIDs& albumsIds, 
         const ServiceData& sourceData);

/**
 * Shows popup, when new version is available 
 * asking user if he wan'ts to download it.
 * If yes then it will open link to it in a new browser window
 *
 */
void showNewVersionAvailablePopup();

} // !namespace facebook

#endif