#ifndef  NOTIFICATIONSDATA_H
#define  NOTIFICATIONSDATA_H

#include "ChangesTypes.h"

namespace facebook{

  /**
   * class NotificationsData
   *  
   * Holds data about number of 
   * different notifications
   */

class NotificationsData{

   // construction
public:
  /*** trivial constructor */
  NotificationsData();

  NotificationsData(const size_t pokesCount, const size_t messageCount, 
    const  size_t eventsCount, const size_t groupsInvsCount,
    const UsersIDs& usersRequestedFriendShipIDs);

   // methods
public:

  size_t getPokesCount() const {
    return pokesCount_;
  }

  size_t getMessageCount() const {
    return messageCount_;
  }

  size_t getRequestsCount() const {
    return usersRequestedFriendShipIDs_.size();
  }

  size_t getEventsCount() const{
    return eventsCount_;
  }

  size_t getGroupsInvsCount() const{
    return groupsInvsCount_;
  }

  UsersIDs getUsersRequestedFriendShipIDs() const{
    return usersRequestedFriendShipIDs_;
  }

   // members
private:
  size_t pokesCount_; // count of pokes
  size_t messageCount_; // count of messages
  size_t eventsCount_; // events  current user invited count
  size_t groupsInvsCount_; // groups invitations count

  UsersIDs usersRequestedFriendShipIDs_; // vector of user id`s
}; 

} // !namespace facebook


#endif